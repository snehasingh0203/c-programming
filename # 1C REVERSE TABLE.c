// WAP TO PRINT MULTIPLICATION TABLE OF A GIVEN NUMBER IN REVERSE ORDER

#include<stdio.h>
int main()
{
	printf("THE TABLE OF ANY NUMBER CAN BE WRITTEN IN REVERSE ORDER AS FOLLOW :- \n\n");
	
	 int a=10;
      
	while(a>=1)
	{
	printf("7x %d = %d\n",a,a*7);
		a--;
      }
	return 0;
}
// TO WRITE THE TABLE OF ANY OTHER NUMBER JUST REPLACE THE NUMBER 7 BY ANY OTHER NUMBER

