// ARRAY INITIALIZATION ----------

#include<stdio.h>
int main()
{
	int s[] = { 32, 23, 134 } ; // INITIALIZING AN ARRAY
	
printf("THE VALUE OF s[0] is : %d \n", s[0]);
printf("THE VALUE OF s[1] is : %d \n", s[1]);
printf("THE VALUE OF s[2] is : %d \n", s[2]);

printf("\n\n");

// INITIALIZING A FLOATING ARRAY	
float a[] = { .32, 23.23, 134.134 } ; 

printf("THE VALUE OF a[0] is : %f \n", a[0]);
printf("THE VALUE OF a[1] is : %f \n", a[1]);
printf("THE VALUE OF a[2] is : %f \n", a[2]);
	
	return 0;
}
